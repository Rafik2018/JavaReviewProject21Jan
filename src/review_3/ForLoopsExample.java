public class ForLoopsExample {


    public static void main(String[] args) {


        for (int i = 10; i > 5; i--) {
            System.out.println(i);
        }

        System.out.println("--------------------------");



        for (int i=5; i<=1; i++){
            System.out.println("Happy");
        }

        System.out.println("--------------------------");



        for (int i =1; i<=6; i++){   // if condition is  true java goes inside if

            if (i%2==0){

                System.out.println("even number "+i);








            }
        }










    }   }
